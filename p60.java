class  p60
{
	public static void main(String[] args) 
	{
		int a = 100, b=50 ; 
		char operator = '-' ; 
		switch (operator)
		{
		case '+':
			   int sum= a+b ; 
		       System.out.println("Sum="+sum);
			   break;
	
		
		case '-':
			   int diff= a-b ; 
		       System.out.println("diff="+diff);
			   break;
		
		case '*':
			   int prod= a*b ; 
		       System.out.println("prod="+prod);
			   break;
		}
	}
}
